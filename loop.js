// The While loop
```
while (condition){
}
```

let i = 0;
while (i < 3) { // shows 0, then 1, then 2
  alert( i );
  i++;
}

// The Do while loop

let i1 = 0;
do {
  alert( i1 );
  i1++;
} while (i1 < 3);

// The for loop

```
for (let i = 0; i < 3; i++) { // shows 0, then 1, then 2
  alert(i);
}

```